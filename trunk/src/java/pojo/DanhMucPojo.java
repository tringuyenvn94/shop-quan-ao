package pojo;

/**
 *
 * @author luctanbinh
 */
public class DanhMucPojo {
    private int maDanhMuc;
    private String tenDanhMuc;
    private String xuatXu;

    public DanhMucPojo(){
        
    }
            
    public DanhMucPojo(String ten, String xuatXu) {
        tenDanhMuc = ten;
        this.xuatXu = xuatXu;
    }

    /**
     * @return the maDanhMuc
     */
    public int getMaDanhMuc() {
        return maDanhMuc;
    }

    /**
     * @param maDanhMuc the maDanhMuc to set
     */
    public void setMaDanhMuc(int maDanhMuc) {
        this.maDanhMuc = maDanhMuc;
    }

    /**
     * @return the tenDanhMuc
     */
    public String getTenDanhMuc() {
        return tenDanhMuc;
    }

    /**
     * @param tenDanhMuc the tenDanhMuc to set
     */
    public void setTenDanhMuc(String tenDanhMuc) {
        this.tenDanhMuc = tenDanhMuc;
    }

    /**
     * @return the xuatXu
     */
    public String getXuatXu() {
        return xuatXu;
    }

    /**
     * @param xuatXu the xuatXu to set
     */
    public void setXuatXu(String xuatXu) {
        this.xuatXu = xuatXu;
    }
}
