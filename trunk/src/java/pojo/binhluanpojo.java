package pojo;

/**
 *
 * @author luctanbinh
 */
public class binhluanpojo {
    private int maBinhLuan;
    private String noiDung;
    private String maNguoiDung;
    private int maSP;

    /**
     * @return the maBinhLuan
     */
    public int getMaBinhLuan() {
        return maBinhLuan;
    }

    /**
     * @param maBinhLuan the maBinhLuan to set
     */
    public void setMaBinhLuan(int maBinhLuan) {
        this.maBinhLuan = maBinhLuan;
    }

    /**
     * @return the noiDung
     */
    public String getNoiDung() {
        return noiDung;
    }

    /**
     * @param noiDung the noiDung to set
     */
    public void setNoiDung(String noiDung) {
        this.noiDung = noiDung;
    }

    /**
     * @return the maNguoiDung
     */
    public String getMaNguoiDung() {
        return maNguoiDung;
    }

    /**
     * @param maNguoiDung the maNguoiDung to set
     */
    public void setMaNguoiDung(String maNguoiDung) {
        this.maNguoiDung = maNguoiDung;
    }

    /**
     * @return the maSP
     */
    public int getMaSP() {
        return maSP;
    }

    /**
     * @param maSP the maSP to set
     */
    public void setMaSP(int maSP) {
        this.maSP = maSP;
    }
}
